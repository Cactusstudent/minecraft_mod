package net.archasmiel.thaumcraft.materials.aspect;

public class ColorCollection {

    private Float r, g, b;

    public ColorCollection(Float r, Float g, Float b) {
        this.r = r;
        this.g = g;
        this.b = b;
    }



    public Float getR() {
        return r;
    }

    public void setR(Float r) {
        this.r = r;
    }

    public Float getG() {
        return g;
    }

    public void setG(Float g) {
        this.g = g;
    }

    public Float getB() {
        return b;
    }

    public void setB(Float b) {
        this.b = b;
    }

}
